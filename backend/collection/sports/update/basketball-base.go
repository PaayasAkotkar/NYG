	ne2000, nc2000, nf2000, nb2000, ni2000 := n2000.Manager(NBA2000OKey)
	ne2004, nc2004, nf2004, nb2004, ni2004 := n2004.Manager(NBA2004OKey)
	ne2008, nc2008, nf2008, nb2008, ni2008 := n2008.Manager(NBA2008OKey)
	ne2011, nc2011, nf2011, nb2011, ni2011 := n2011.Manager(NBA2011Key)
	ne2012, nc2012, nf2012, nb2012, ni2012 := n2012.Manager(NBA2012Key)

	parcel1 := SportsParcel{Event: ne1991, Category: nc1991, Field: nf1991, Book: nb1991, MappedItems: ni1991}
	parcel2 := SportsParcel{Event: ne1992, Category: nc1992, Field: nf1992, Book: nb1992, MappedItems: ni1992}
	parcel3 := SportsParcel{Event: ne1996, Category: nc1996, Field: nf1996, Book: nb1996, MappedItems: ni1996}
	parcel4 := SportsParcel{Event: ne2000, Category: nc2000, Field: nf2000, Book: nb2000, MappedItems: ni2000}
	parcel5 := SportsParcel{Event: ne2004, Category: nc2004, Field: nf2004, Book: nb2004, MappedItems: ni2004}
	parcel6 := SportsParcel{Event: ne2008, Category: nc2008, Field: nf2008, Book: nb2008, MappedItems: ni2008}
	parcel7 := SportsParcel{Event: ne2011, Category: nc2011, Field: nf2011, Book: nb2011, MappedItems: ni2011}
