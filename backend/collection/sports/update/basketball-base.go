func BasketballLists() []string {
	ma := []string{}
	token := BuildBasketball().Pack
	for _, t := range token {
		for items := range t.MappedItems {
			ma = append(ma, items)
		}
	}
	return ma
}

func BasketballSheet() map[string][]string {
	sheet := map[string][]string{}
	token := BuildBasketball().Pack
	for _, t := range token {
		maps.Copy(sheet, t.MappedItems)
	}
	return sheet
}
